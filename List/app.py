names = ['John', 'Bob', 'Mosh', 'Sarah', 'Shahin']
print(names)  # ['John', 'Bob', 'Mosh', 'Sarah', 'Shahin']
print(names[0])  # John
print(names[-1])  # Shahin
print(names[2:])  # ['Mosh', 'Sarah', 'Shahin']
print(names[2:4])  # ['Mosh', 'Sarah']
print(names[:])  # ['John', 'Bob', 'Mosh', 'Sarah', 'Shahin']

names[0] = 'Jon'
print(names)  # ['Jon', 'Bob', 'Mosh', 'Sarah', 'Shahin']



